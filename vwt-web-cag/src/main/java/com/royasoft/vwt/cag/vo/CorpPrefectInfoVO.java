package com.royasoft.vwt.cag.vo;

public class CorpPrefectInfoVO {
    private String corpId;
    private String corpName;
    private String contactPersonName;
    private String contactTel;
    private String regionName;
    private String corpAddress;
    private String corpMemberNumber;

    public String getCorpId() {
        return corpId;
    }

    public void setCorpId(String corpId) {
        this.corpId = corpId;
    }

    public String getCorpName() {
        return corpName;
    }

    public void setCorpName(String corpName) {
        this.corpName = corpName;
    }

    public String getContactPersonName() {
        return contactPersonName;
    }

    public void setContactPersonName(String contactPersonName) {
        this.contactPersonName = contactPersonName;
    }

    public String getContactTel() {
        return contactTel;
    }

    public void setContactTel(String contactTel) {
        this.contactTel = contactTel;
    }

    public String getRegionName() {
        return regionName;
    }

    public void setRegionName(String regionName) {
        this.regionName = regionName;
    }

    public String getCorpAddress() {
        return corpAddress;
    }

    public void setCorpAddress(String corpAddress) {
        this.corpAddress = corpAddress;
    }

    public String getCorpMemberNumber() {
        return corpMemberNumber;
    }

    public void setCorpMemberNumber(String corpMemberNumber) {
        this.corpMemberNumber = corpMemberNumber;
    }

}
